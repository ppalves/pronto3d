      function getFormJSON(formArray) {
        var obj = {};
        jQuery.each(formArray, function(i, pair) {
          var cObj = obj, pObj, cpName;
          jQuery.each(pair.name.split("."), function(i, pName) {
            pObj = cObj;
            cpName = pName;
            cObj = cObj[pName] ? cObj[pName] : (cObj[pName] = {});
          });
          pObj[cpName] = pair.value;
        });
        return obj;
      }
