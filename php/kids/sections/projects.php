            <p>
            Como estudo para identificação das formas válidas e corretas na linguagem, os jogos de lógica se dedicam a identificar e qualificar aquilo que não possui uma validade formal coesa e correta. O jogo do Desafio dos Sapos é um teste simples de raciocínio lógico em que se troca os sapos de lado de um tabuleiro, os movendo na sequência correta para conseguir um resultado válido. A oficina tem estrutura totalmente prática em que os alunos recebem explicações sobre lógica e como praticar no jogo desenvolvido (Desafio do Sapo). Paralelamente, recebem instruções sobre a produção automatizada de materialização da forma visando o corte laser de papel e montagem manual para a obtenção das peças do jogo. Os alunos tem a oportunidade de conhecer as tecnologias de corte e acompanhar a produção das peças que serão por eles montadas para o desafio.
            </p>
